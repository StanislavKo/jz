package com.hsd.jz.server.security.pojo;

public class UidCredentials {

	private String uid;

	public UidCredentials() {
		super();
	}

	public UidCredentials(String uid) {
		super();
		this.uid = uid;
	}

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

}
